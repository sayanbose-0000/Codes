# Normally packages are installed for whole computer. We use virtual env to install for current project only

# virtual environment allow us to install modules that we don't wnat to install for the entire computer. It allows us to install a module just for the particular project. Eg- anaconda, virtualenvwrapper. note: acadonda is not just a virtual environmnet software, it is a machine learning software

# note: in the entire pc, we can have only one virtual environment of the same name

