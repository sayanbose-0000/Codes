CREATE TABLE Dept (
  deptno NUMBER PRIMARY KEY,
  dname VARCHAR2(20),
  loc VARCHAR2(20)
);

CREATE TABLE Emp (
  empno NUMBER PRIMARY KEY,
  ename VARCHAR2(20),
  job VARCHAR2(20),
  mgr NUMBER,
  hiredate DATE,
  sal NUMBER,
  comm NUMBER,
  deptno NUMBER,
  FOREIGN KEY (deptno) REFERENCES Dept(deptno)
);


